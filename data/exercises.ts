// 工具函数
export function getExercisesByIds(ids: string[]): Exercise[] {
  return ids
    .map((id) => exerciseLibrary.find((e) => e.id === id))
    .filter((e): e is Exercise => e !== undefined);
}

export function getExerciseById(id: string): Exercise | undefined {
  return exerciseLibrary.find((e) => e.id === id);
}

export function getExercisesByMuscle(muscle: string): Exercise[] {
  return exerciseLibrary.filter(
    (e) =>
      e.primaryMuscles.includes(muscle as any) ||
      e.secondaryMuscles.includes(muscle as any)
  );
}

export function getExercisesByCategory(category: 'compound' | 'isolation'): Exercise[] {
  return exerciseLibrary.filter((e) => e.category === category);
}
